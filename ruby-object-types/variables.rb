# Variables

# exception to the rule that everything is an object - store a reference to an object

x = 1 # assignment of a value
y = x + 2

# convention for writing variables is lowercase with underscores

first_name = "Bob"

# best practice is to use readable names for variables
customer_number = 1245

# Variable types
# =========================
# Global - $variable
# Class - @@variable
# Instance - @variable
# Local - variable
